export default class Logger {

    /**
     * @param {string} message
     */
    log(message) {
        console.log(message);
    }

    /**
     * @param {string} message
     */
    debug(message) {
        console.log(message);
    }

    /**
     * @param {string} message
     */
    error(message) {
        console.log(message);
    }

}
